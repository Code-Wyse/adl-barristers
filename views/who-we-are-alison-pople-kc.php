<p><strong>Areas of expertise:</strong></p>

<p><strong>Bribery and Corruption:&nbsp;</strong>Alison has acted in a significant number of Bribery Act 2010 and pre-Bribery Act investigations conducted by the SFO, including corporate investigations&nbsp;and contested UK criminal proceedings relating to domestic and international corruption.&nbsp;Alison acted in respect of an approved Deferred Prosecution Agreement reached with the SFO&nbsp;relating to corporate offences of failing to prevent bribery.</p>

<p><strong>Fraud:&nbsp;</strong>Alison has advised and represented many clients investigated for or charged with&nbsp;Fraud Act 2006 offences and other associated offences by either the SFO or the CPS. &nbsp;She&nbsp;has also advised on the prosecution of Fraud Act matters.</p>


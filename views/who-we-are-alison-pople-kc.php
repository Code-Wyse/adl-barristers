<p><strong>Insider Dealing:&nbsp;</strong>Alison has advised on a number of insider dealing investigations undertaken by the FCA, and represented clients in three separate criminal cases&nbsp;brought by the FCA.&nbsp;</p>

<p><strong>Cartels:</strong>&nbsp;Alison&nbsp;successfully represented an individual charged with the criminal cartel offence by the OFT (now CMA) under the Enterprise Act 2002, as well as successfully avoiding prosecution for the cartel offence in two other separate cases. Alison is also experienced in advising clients subject to&nbsp;civil cartel investigations undertaken by the CMA.</p>

<p><strong>Proceeds of Crime:</strong>&nbsp;Alison has wide-ranging&nbsp;experience&nbsp;advising and representing clients facing legal action or investigation&nbsp;under the Proceeds of&nbsp;Crime Act 2002, including Part 2 (confiscation), Part 5 (civil recovery), Part 7 (money laundering) and Part 8 (investigations) as well as advising corporate clients and firms&nbsp;on all aspects of&nbsp;the Money Laundering Regulations.</p>

<p><strong>Professional Discipline:&nbsp;</strong>Alison has acted for a wide range of professionals including solicitors, accountants, medical professionals and police officers under investigation by their professional supervisory bodies,&nbsp;both in tribunal proceedings and in an advisory capacity. She is skilled in preparing successful&nbsp;early representations to avoid proceedings.</p>

<p><strong>Regulatory:</strong>&nbsp;Alison is&nbsp;experienced in&nbsp;addressing a broad spectrum of financial&nbsp;regulatory issues either as the main issue or ancillary to criminal investigations. She is adept at addressing niche regulatory issues raised by a particular business sector; examples of her prior advisory work include&nbsp;the regulation of medicines and healthcare products and the regulation of assessment&nbsp;of varying rates of landfill tax.</p>


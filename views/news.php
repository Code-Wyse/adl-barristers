	<p>Nicholas Purnell QC acts for Verres, the former governor of Sicily.</p>

<p>Verres served as governor of Sicily (a wealthy and fertile province) between 73 and 71 BC. He allegedly took the opportunity to plunder the province, by imposing extortionate taxes, robbing temples and other abuses.</p>

<p>A Roman governor acted as lord chief justice of his province. This was very handy for Verres. He was able to convict and sentence people at whim. He could then demand massive bribes for releasing them.</p>

<p>On returning to Rome in 70 BC, Verres stood trial for extortion and abusing his position as governor. The trial caused a sensation, with ace counsel on both sides. Cicero prosecuted and Hortensius defended. The trial did not go well for the defence, so Verres took flight. The trial was never concluded.</p>

<p>Now, 2088 years later, Verres will face a fresh trial. This time there will be no escape.</p>

<p>Lady Hale, President of the Supreme Court, will preside.</p>


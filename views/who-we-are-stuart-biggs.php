<p>Stuart&rsquo;s practice focuses on matters involving allegations of misconduct by companies, directors, public officials and financial professionals.</p>

<p>He acts for individuals and corporates investigated and prosecuted by the SFO, FCA, HMRC and specialist divisions of the CPS and advises victims of financial crime.&nbsp; He assists corporates with internal investigations and in their interactions with the criminal justice system and regulators.&nbsp; He has considerable experience in Proceeds of Crime Act proceedings.</p>

<p>Stuart advises on a range of matters affecting companies including bribery, fraud, anti-money laundering, sanctions, data protection, brand and content protection and consumer protection.&nbsp; He has a wealth of prosecution experience to draw upon and maintains a specialist prosecution practice, including private prosecutions.</p>

<p>He has complementary experience of advising and acting in respect of individual and company insolvency and has acted for a director in contested company director disqualification proceedings.&nbsp; He has appeared in cases before the tax tribunals as leading junior, led junior and alone.</p>

<p>Since co-authoring the Butterworths&rsquo; Guide to the Proceeds of Crime Act 2002 Stuart has maintained a keen interest in this area, conducting confiscation, restraint and enforcement hearings. &nbsp;Recently, he was invited to contribute to the Law Commission&rsquo;s work on suggested reform of the law of confiscation.&nbsp; He has experience in dealing with orders obtained on behalf of foreign prosecutors by MLA, acting in the high-profile successful discharge application of a restraint order obtained on behalf of the Vatican.&nbsp; He has considerable experience in account freezing and cash forfeiture cases: recent matters have seen the release of the trading accounts of a bitcoin brokerage and a construction company.</p>

<p>Stuart has a longstanding specialism in Intellectual Property Crime and content protection.&nbsp; He is instructed by a variety of rightsholders and provides detailed advice on the substantive law and on investigations and procedure.&nbsp; He has spoken on IP crime at the Cambridge Symposium on Economic Crime.</p>

<p>Stuart acts for clients in proceedings before a diverse range of disciplinary tribunals and regulators.&nbsp; These have included governing bodies in respect of the accountancy profession, the Regulatory Decisions Committee of the FCA, the Electoral Commission and the internal disciplinary committee of a political party.</p>


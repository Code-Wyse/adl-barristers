    <p><strong>How long will your personal data be retained for? </strong></p>
    <p>We will not keep your personal data in a form that  identifies you for longer than is necessary for the purposes set out in this  notice or as required or permitted by applicable law. Unless specific  circumstances require us to keep it (e.g. for maintaining conflict checks) we  will delete, destroy or anonymise your information around 7 years after the end  of processing, e.g. the conclusion of a case/matter or receipt of final  payment, or writing off of fees, whichever is the latest.</p>
    <p><strong>Transfers  of personal data outside of the UK</strong></p>
    <p>The nature of the matter which requires the processing may  mean that it is necessary to  transfer  your data to a location (for example to a secure server) outside the UK, for  processing  purposes set out in this  notice. This may include circumstances where we are instructed to act on a case  by or involving lawyers located outside of the UK.</p>
    <p>In such cases, to safeguard your privacy rights, transfers  will be carried out with appropriate safeguards to ensure the safety and  security of your information. This may include transfers to recipients to which  a European Commission &lsquo;adequacy decision&rsquo; applies or the transfers will be  carried out under standard contractual clauses that have been approved by the  European Commission as providing appropriate safeguards for international  personal data transfers.</p>
    <p><strong>Your rights </strong></p>
    <p>The UK GDPR gives specific rights in terms of personal data: </p>
    <ul>
      <li>The right   to be informed about  the  information we hold and what we use it for and the right to ask for access to  the personal information we hold about you. </li>
      <li>A request can be made that personal data held  is erased; </li>
      <li>A right to ask us to correct any inaccuracies  with the personal data we hold. </li>
      <li>You can ask us to stop sending you direct mail  or emails or, in some circumstances, ask us to stop processing your details; </li>
      <li>The right to restrict how data is processed;</li>
      <li>You have the right to have a decision taken by  a human (however, the way in which we process data is unlikely to involve  automated means to process that data particularly where the data has a legal or  similarly significant effect on you); </li>
      <li>the right to be notified of a personal data  breach if it results in a high risk to your rights and freedoms; </li>
      <li>the right to withdraw your consent if you have  given your consent to processing of any of your personal data (please note that  if you withdraw your consent, this will not affect the validity of any  processing carried out prior to the withdrawal); and</li>
      <li>where your personal data is processed by  automated means, you can ask the data to be transmitted to another data  controller. If you request this, you will be informed whether this is possible,  taking into account compatibility of systems of the other data controller to  whom you wish the transfer to be made.</li>
    </ul>
    <p>These rights are also subject to certain limitations that  exist in law. This is particularly the case when the data processing relates to  the pursuit or defence of legal rights.</p>
    <p>Depending on the nature and extent of your request, the  contractual relationship with you may need to end as we would be unable to  continue to deliver legal services without being able to process personal data  in the way needed. In this event, you will remain liable for any outstanding  fees and disbursements incurred before the request was made.</p>

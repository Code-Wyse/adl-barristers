    <p>We process personal data about our lay and professional  clients, potential clients, about individuals who feature in the matters in  respect of which we are asked to provide legal services, witnesses and experts,  opponents, other lawyers with whom we are working, court staff and members of the  judiciary and others connected to the matters we are working on.</p>
    <p><strong>What  data do we process about you?</strong></p>
    <p>Depending on the type of work, we collect and process both  personal data and special categories of personal data as defined in the UK  GDPR. This may include:</p>
    <ul>
      <li>Name, email address, phone number, address</li>
      <li>Payment or bank details </li>
      <li>Date of birth</li>
      <li>Next of kin and family details</li>
      <li>Details pertaining to education,  employment and professional or regulatory  arrangements</li>
      <li>Information on your background &amp; current  circumstances </li>
      <li>Financial information</li>
      <li>Identification documents including photographic  data</li>
      <li>Interests and preferences</li>
    </ul>
    <p>Where relevant, we may also need to process special  category personal data that reveals your:</p>
    <ul>
      <li>Racial or ethnic origin</li>
      <li>Political opinions</li>
      <li>Religious and philosophical beliefs</li>
      <li>Trade union membership</li>
      <li>Genetic data</li>
      <li>Biometric data for the purpose of uniquely  identifying a natural person</li>
      <li>Data concerning health, both physical and  mental </li>
      <li>Sex life and sexual orientation.</li>
    </ul>
    <p>We also process personal data relating to criminal  convictions and offences.</p>
    <p><strong>Our  lawful basis for processing your information</strong></p>
    <p>In order that we can provide legal services and  representation for you, we must process your personal data. The UK General Data  Protection Regulation (the UK GDPR) requires that where we process personal  data, we must have a lawful basis for doing so. The lawful bases identified in  the UK GDPR that we seek to rely upon are as follows:</p>
    <p><strong>Contractual  necessity</strong></p>
    <p>We will process your personal data on the basis that it is  necessary to enable us to fulfil our contractual duties to you or to take steps  to enter into a contract with you.</p>
    <p><strong>Compliance  with a legal obligation</strong></p>
    <p>We process personal data to enable us to comply with  applicable laws. These include</p>
    <ul>
      <li>To make statutory VAT and tax returns to HMRC</li>
      <li>To comply with various regulatory and  professional obligations governing the work we do</li>
    </ul>
    <p><strong>Legitimate interests </strong></p>
    <p>We process personal data for our legitimate business  interests which include but are not limited to:</p>
    <ul>
      <li>Provision of legal services and advice </li>
      <li>For purposes of practice administration and  management, accounting, fee collection, banking and debt recovery.</li>
      <li>For completion of professional regulatory  requirements. </li>
      <li>Processing for marketing purposes.</li>
      <li>Processing to prevent fraud.</li>
      <li>To ensure our network and systems are secure. </li>
      <li>Reporting threats to public security.</li>
    </ul>
    <p><strong>Consent </strong></p>
    <p>Where this is our lawful reason for processing, we will  ensure that we have your specific consent for processing your data for the  specified purposes. You will also have the right to withdraw your consent at  any time.</p>
    <p>Where you do so this will not affect the legality of data  processing which had taken place prior to your withdrawal of consent.</p>
    <p><strong>Special category processing </strong></p>
    <p>The UK GDPR specifies that where we process special  category data, we must rely upon certain exemptions in order to do so lawfully.  The following exemptions are applicable in our practice</p>
    <ul>
      <li>We have your explicit consent to do so;</li>
      <li>It is necessary for the exercise or defence of  legal claims or judicial acts; and</li>
      <li>For reasons of substantial public interest.</li>
    </ul>
    <p><strong>Criminal data processing </strong></p>
    <p>When we process data relating to criminal offences  it is necessary for:</p>
    <ul>
      <li>The purpose of, or in connection with, any  legal proceedings (including prospective legal proceedings);</li>
      <li>The purpose of obtaining legal advice;</li>
      <li>The purposes of establishing, exercising or  defending legal rights;</li>
      <li>Complying with regulatory requirements relating  to unlawful acts and dishonesty; </li>
      <li>Suspicion of terrorist financing or money  laundering; </li>
      <li>Publication of legal judgements; and/or</li>
      <li>Where we have your explicit consent to do so.</li>
    </ul>
    <p><strong>Purposes </strong></p>
    <p>We may use your personal information for the following  purposes:</p>
    <ul>
      <li>Provide legal advice and representation; </li>
      <li>Investigate and address your concerns;</li>
      <li>Communicate with you about news, updates and  events; </li>
      <li>Investigate or address legal proceedings  relating to your use of our services, or as otherwise allowed by applicable  law;</li>
      <li>maintain internal records, including about  terminated contracts;</li>
      <li>ensure good governance, accounting, management  and auditing; </li>
      <li>Make any required statutory returns;</li>
      <li>Assist in any tendering or panel membership  applications; Assist in any other applications for the purpose of professional  development or career progression;</li>
      <li>Communicate legal updates and judgments to  other legal professionals;</li>
      <li>For marketing purposes;</li>
      <li>The management and administration of our  practice; </li>
      <li>To recover debt;</li>
      <li>To communicate and manage complaints with  regulators; and/or</li>
      <li>Where relevant to conduct anti money  laundering, terrorist financing or conflict of interest checks.</li>
    </ul>

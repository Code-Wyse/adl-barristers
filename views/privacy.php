      <li>Our chambers IT providers;</li>
      <li>Expert witnesses and other witnesses;</li>
      <li>Our regulator and/or legal advisors in the  event of a dispute, complaint or other legal matter;</li>
      <li>Head of Chambers or complaints representatives  within our chambers, in the event of a complaint;</li>
      <li>Law enforcement officials, government authorities,  or other third parties, to meet any legal obligations;</li>
      <li>Legal directories, for the purpose of  professional development;</li>
      <li>Any relevant panel or tendering committee, for  the purpose of professional development;</li>
      <li>Accountants and banking officials;</li>
      <li>Regulators or arbitrators, where complaints or  disputes arise; or</li>

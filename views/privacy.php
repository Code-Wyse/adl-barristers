    <p><strong>Introduction</strong></p>
    <p>In order to provide legal advice and representation, we  need to collect and hold personal information. This means that steps have to be  taken by us to protect this data under data protection legislation known as UK  GDPR. This may be your personal data or information relating to other parties  involved in the matter. We will take all possible steps to protect personal  information. We will ensure that we do not do anything that may infringe your  rights or undermine your trust. This privacy notice describes the information  which the self-employed barristers collect about you, how it is used and  shared, and your rights regarding it.</p>

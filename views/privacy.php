    <p>For the purposes set out in this notice, and subject to our  professional obligations of confidentiality and the legal professional  privilege of our clients, we may provide your personal data to the following:</p>
    <ul>
      <li>Instructing solicitors or other lawyers  involved in your case, including those with whom we are working;</li>
      <li>Opposing legal representatives;</li>
      <li>Members of the Judiciary and court official; </li>
      <li>Opposing lay clients;</li>

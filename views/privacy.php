    <p><strong>Data  collection</strong></p>
    <p>The vast majority of the information that we hold about you  is provided to, or gathered by, us in the course of your case and/or  proceedings. Either we or your solicitor will tell you why we need the  information and how we will use it. In addition to the information you may  provide to us or your solicitor, we also obtain information from other sources  as follows:</p>
    <ul>
      <li>Information that is available publicly in  registers, searches or in the media</li>
      <li>Other legal professionals including solicitors  and barristers and their associates, trainees and staff</li>
      <li>Chambers staff </li>
      <li>Expert witnesses </li>
      <li>Prosecution bodies</li>
      <li>Regulatory, public or administrative bodies </li>
      <li>Court staff &amp; officials</li>
